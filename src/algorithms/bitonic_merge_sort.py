# Implementation of bitonic merge sort algorithm

class BitonicMergeSort:
    complexity = "O(logn^2)"
    parallel = True
    
    def __init__(self, seq, ps_n=1):
        self.seq = seq

    def sort(self, a):
        return a
    
    def run(self):
        return self.sort(self.seq)